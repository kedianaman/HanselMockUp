import Foundation
import UIKit

extension UIColor {
    class func hanselBlack() -> UIColor {
        return UIColor(displayP3Red: 34/255.0, green: 38/255.0, blue: 39/255.0, alpha: 1.0)
    }
    
    class func hanselAqua() -> UIColor {
        return UIColor(displayP3Red: 0/255.0, green: 204/255.0, blue: 179/255.0, alpha: 1.0)
    }
    
    class func hanselNavy() -> UIColor {
        return UIColor(displayP3Red: 45/255.0, green: 58/255.0, blue: 75/255.0, alpha: 1.0)
    }
    
    
    class func hanselDarkGrey() -> UIColor {
        return UIColor(displayP3Red: 181/255.0, green: 181/255.0, blue: 181/255.0, alpha: 1.0)

    }
    
    class func hanselLightGrey() -> UIColor {
        return UIColor(displayP3Red: 234/255.0, green: 234/255.0, blue: 234/255.0, alpha: 1.0)
    }
    
    class func hanselGreyTextColor() -> UIColor {
        return UIColor(displayP3Red: 187/255.0, green: 198/255.0, blue: 206/255.0, alpha: 1.0)
    }
    
    
}
